public class TwoNumbers {
  public static void main(String[] args) {
	
	int myNumA = 1;
	int myNumB = 15;
	  
	if (myNumA > myNumB) {
		System.out.println("myNumA is greater than myNumB");
	}else if (myNumA < myNumB) {
		System.out.println("myNumB is greater than myNumA");
	} else {
		System.out.println("myNumB and myNumA are the same");
	}
    
	System.out.println("Hello World");
  }
}